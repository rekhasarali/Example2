package Day2;

public class IncrementandDecrementOperators {

	public static void main(String[] args) {
		// increment operators (++)
		int a =10;
		a++; //a=a+1;
		System.out.println(a);
		
		
		// Decrement operators (--)
		int v =100;
		//a--; , //--a;
		 v = v-1;
		System.out.println(v);
		
		//Assignment Operators
		int i=10;
		i=i+5;
		//i+=5;
		System.out.println(i);//15
		int k=10;
		k-=5;
		//i=i-5;
		System.out.println(k);//5
		
		
		//Ternary Operators
		int c=1000,d=2000;
	int z=d>c? d:c;
	System.out.println(z);//2000
	int e=2000,f=3000;
  int y=e>f? e:f;
   System.out.println(y);//3000

	
	

		
		
	
		
		
		
		
		
		
   
     


		

	
		

	}

}
